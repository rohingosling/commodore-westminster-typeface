// Each record is 9 bytes: 
//
// - One screen-code byte followed by eight packed glyph bytes (row 0 first, 
//   MSB-left: column 0 = bit 7). 
//
// - The installer stamps each record into the RAM charset at  charset_base + 
//   screen_code * 8.
//
// To redefine a glyph, edit the eight glyph bytes on its .byte line below. The
// 8-row picture above each record shows the shape those bytes draw ('X' = pixel
// on).
//
// - It is a visual guide only.
//
// - The bytes are what assemble. If you add or remove records, update 
//   WESTMINSTER_OVERLAY_COUNT to match.
//
//-----------------------------------------------------------------------------

